text = open("input.txt", "r", encoding="utf-8").read()
chars = sorted(list(set(text)))
stoi = {ch: i for i, ch in enumerate(chars)}
itos = {i: ch for ch, i in stoi.items()}
vocab_size = len(chars)
data = torch.tensor([stoi[ch] for ch in text], dtype=torch.long)

class NextTokenDataset(Dataset):
    def __init__(self, data, block_size):
        self.data = data
        self.block_size = block_size
    def __len__(self):
        return len(self.data) - self.block_size
    def __getitem__(self, idx):
        x = self.data[idx : idx + self.block_size]
        y = self.data[idx + 1 : idx + self.block_size + 1]
        return x, y

block_size = 32
dataset = NextTokenDataset(data, block_size)
loader = DataLoader(dataset, batch_size=64, shuffle=True)
xb, yb = next(iter(loader))

"""## 1. Single-head masked self-attention"""

class SingleHeadSelfAttention(nn.Module):
    def __init__(self, emb_dim, block_size):
        super().__init__()
        self.key = nn.Linear(emb_dim, emb_dim, bias=False)
        self.query = nn.Linear(emb_dim, emb_dim, bias=False)
        self.value = nn.Linear(emb_dim, emb_dim, bias=False)
        self.register_buffer("tril", torch.tril(torch.ones(block_size, block_size)))

    def forward(self, x):
        B, T, C = x.shape
        k = self.key(x) # к°Ғк°Ғмқ„ linearлЎң л§Ңл“Өм–ҙмӨҢ
        q = self.query(x)
        v = self.value(x)


        # л°‘м—җ м„ё мӨ„мқҙ н•өмӢ¬ attention
        wei = q @ k.transpose(-2, -1) * (C ** -0.5)
        # wei[b,t,s] = bлІҲм§ё batchм—җм„ң tлІҲм§ё мң„м№ҳк°Җ sлІҲм§ё мң„м№ҳлҘј м–јл§ҲлӮҳ м°ёкі н• м§Җм—җ лҢҖн•ң м җмҲҳ
        # (c**-0.5)лҠ” scaling. dimensionмқҖ ліҖн•ҳм§Җ м•ҠмқҢ
        wei = wei.masked_fill(self.tril[:T, :T] == 0, float("-inf")) # лҜёлһҳлҘј ліҙм§Җ лӘ»н•ҳкІҢ н•ҳлҠ” mask
        wei = F.softmax(wei, dim=-1) # л§Ё л’Өм—җ кІғмқ„ кё°мӨҖмңјлЎң нҷ•лҘ  м„Өм •


        out = wei @ v
        # out[b,t,:] : tлІҲм§ё мң„м№ҳк°Җ м°ёкі  к°ҖлҠҘн•ң кіјкұ° мң„м№ҳл“Өмқҳ value vectorлҘј к°ҖмӨ‘нҸүк· н•ң кІ°кіј

        return out

"""## 2. Attention нҸ¬н•Ё мөңмҶҢ лӘЁлҚё"""

class TinyAttentionLM(nn.Module):
    def __init__(self, vocab_size, block_size, emb_dim=64):
        super().__init__()
        self.token_embedding = nn.Embedding(vocab_size, emb_dim)
        self.position_embedding = nn.Embedding(block_size, emb_dim)
        self.attn = SingleHeadSelfAttention(emb_dim, block_size)
        self.lm_head = nn.Linear(emb_dim, vocab_size)

    def forward(self, x):
        B, T = x.shape
        pos = torch.arange(T, device=x.device) # мң„м№ҳ лІҲнҳё
        tok = self.token_embedding(x)
        pos = self.position_embedding(pos)[None] # [None] : л§Ё м•һ dimensionмқ„ н•ҳлӮҳ м¶”к°Җ [64,32] -> [1,64,32]. unsquezeeлһ‘ л№„мҠ·
        h = tok + pos # мӣҗлһҳлҠ” broadcastingм—җ мқҳн•ҙ мһҗлҸҷмңјлЎң лҚ”н•ҙм§Җм§Җл§Ң, мң„м—җм„ң лӘ…мӢңм ҒмңјлЎң dimensionмқ„ л§һм¶°мӨҢ
        h = self.attn(h)
        logits = self.lm_head(h)
        return logits

model = TinyAttentionLM(vocab_size, block_size)
logits = model(xb)
print("logits.shape:", logits.shape)

"""## 3. н•ҷмҠө"""

def sequence_cross_entropy(logits, targets):
    return F.cross_entropy(logits.transpose(1, 2), targets)

def train_one_epoch(model, loader, optimizer, device, max_steps=None):
    model.train()
    total_loss, total_count = 0.0, 0
    for step, (xb, yb) in enumerate(loader):
        xb, yb = xb.to(device), yb.to(device)
        logits = model(xb)
        loss = sequence_cross_entropy(logits, yb)
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()
        total_loss += loss.item() * xb.size(0)
        total_count += xb.size(0)
        if max_steps is not None and step + 1 >= max_steps:
            break
    return total_loss / total_count

device = "cuda" if torch.cuda.is_available() else "cpu"
model = TinyAttentionLM(vocab_size, block_size).to(device)
optimizer = torch.optim.AdamW(model.parameters(), lr=1e-3)

for epoch in range(100):
    train_loss = train_one_epoch(model, loader, optimizer, device, max_steps=300)
    print(f"epoch {epoch:2d} | train loss {train_loss:.4f}")

"""## 4. Sampling"""

@torch.no_grad()
def sample_attention_model(model, block_size, stoi, itos, device, start_text="ROMEO:", max_new_tokens=300):
    model.eval()
    context = torch.zeros((1, block_size), dtype=torch.long, device=device)
    for ch in start_text:
        if ch in stoi:
            ix = torch.tensor([[stoi[ch]]], device=device)
            context = torch.cat([context[:, 1:], ix], dim=1)
    out = list(start_text)
    for _ in range(max_new_tokens):
        logits = model(context)
        logits = logits[:, -1, :]
        probs = F.softmax(logits, dim=-1)
        ix = torch.multinomial(probs, num_samples=1)
        out.append(itos[ix.item()])
        context = torch.cat([context[:, 1:], ix], dim=1)
    return "".join(out)

print(sample_attention_model(model, block_size, stoi, itos, device, start_text="ROMEO:", max_new_tokens=400))

